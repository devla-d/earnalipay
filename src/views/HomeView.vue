        for (const row of dTable.rows) {
          for (const col of row.cells) {


            if (col.classList[1] === 'deposit-amount') {
              col.innerHTML = iName[Math.floor(Math.random() * iName.length)]
            } else if (col.classList[1] === 'deposit-names') {
              col.innerHTML = `$<span>${Math.floor(Math.random() * (2000 - 50 + 1)) + 50}</span>`
            }


          }
        }



      }, 3000)






      this.intervalii = setInterval(function () {

        for (const row of wTable.rows) {
          for (const col of row.cells) {


            if (col.classList[1] === 'withdraw-amount') {
              col.innerHTML = iName[Math.floor(Math.random() * iName.length)]
            } else if (col.classList[1] === 'withdraw-names') {
