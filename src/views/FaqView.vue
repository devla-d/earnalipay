    document.title = "Faq";
    this.ini_accordion()
    
  },
  methods: {
        ini_accordion(){
            window.$('.accordion >.accordion-item:eq(0) .accordion-title').addClass('active').next().slideDown().parent().addClass('active');
            window.$('.accordion .accordion-title').click(function(j) {
                var dropDown = window.$(this).closest('.accordion-item').find('.question-block');
                console.log(dropDown)
                window.$(this).closest('.accordion').find('.question-block').not(dropDown).slideUp();
                if (window.$(this).hasClass('active')) {
                    window.$(this).removeClass('active').parent().removeClass('active');
                } else {
                    window.$(this).closest('.accordion').find('.accordion-title.active').removeClass('active').parent().removeClass('active');
                    window.$(this).addClass('active').parent().addClass('active');
                }
                dropDown.stop(false, true).slideToggle();
                j.preventDefault();
            });
        

        }
  },
};
</script>

